package com.minidb.optimizer.index;

/**
 * 索引类型
 *
 * MySQL 支持多种索引类型，主要包括聚簇索引和二级索引
 *
 * 对应八股文知识点:
 * ✅ 聚簇索引 vs 非聚簇索引
 * ✅ 主键索引 vs 二级索引
 * ✅ 回表查询的概念
 *
 * @author Mini-MySQL
 */
public enum IndexType {
    /**
     * 聚簇索引（主键索引）
     *
     * 特点:
     * - 叶子节点存储完整的行数据
     * - 一个表只能有一个聚簇索引
     * - 通常使用主键作为聚簇索引
     * - InnoDB 默认使用主键构建聚簇索引
     */
    CLUSTERED,

    /**
     * 二级索引（辅助索引、非聚簇索引）
     *
     * 特点:
     * - 叶子节点存储索引列值 + 主键值
     * - 一个表可以有多个二级索引
     * - 查询时可能需要回表（根据主键再次查询聚簇索引）
     * - 覆盖索引可以避免回表
     */
    SECONDARY
}
